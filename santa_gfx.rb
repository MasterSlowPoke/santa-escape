module SantaGfx
	# METHODS
  def show_splash_screens
    display_nvidia
    display_prompt

    display_title_screen
  end

  def display_nvidia
    puts MIDDLEWARE_LOGO
  end

  def display_title_screen
    puts SANTA_LOGO
  end

  def display_you_win
    puts YOU_WIN_GFX
  end

  def display_you_lose
    puts YOU_DIED_GFX
  end

  def show_ending(game_result)
    display_prompt

    puts "\a" + ENDING_SEPERATOR

    if(game_result == :you_win)
      display_you_win
      puts get_string(:you_win)
    else
      puts get_string(:game_over)
      display_you_lose
      puts get_string(:drugs)
    end

    puts ENDING_SEPERATOR + "\n\n"

    game_result
  end

